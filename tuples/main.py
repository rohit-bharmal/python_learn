# Create a tuple of fruits
fruits = ("apple", "banana", "cherry", "date", "elderberry")
print(fruits)

# Print the tuple using for loop
for fruit in fruits:
    print(fruit)
print("Done")

# Print the tuple using while loop
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1
    
# Print the tuple using index
print(fruits[0]) # Print the first fruit
print("Done")