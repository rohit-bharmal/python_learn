# Operators are the symbols that are used to perform operations on variables and values.
a = 10
b = 20

# Arithmetic operators: +, -, *, /, %, **, //
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)

# Comparison operators: ==, !=, >, <, >=, <=
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical operators: and, or, not
print(a and b)
print(a or b)
print(not a)